    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed

        String nuevaContraseña, confirmacion;
        nuevaContraseña = this.txt_nuevaContraseña.getText();
        confirmacion = this.txt_confirmacion.getText();

        if (!nuevaContraseña.equals("") && !confirmacion.equals("")) {

            if (confirmacion.equals(nuevaContraseña)) {

                try {
                    Class.forName("com.mysql.jdbc.Driver");
                    Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/on_reservationssoftware", "root", "");
                    PreparedStatement pst = cn.prepareStatement("update usuarios set password=? where id_usuario= '" + InfoUsuario.ID_usuario_seleccionado + "'");
                    pst.setString(1, this.txt_nuevaContraseña.getText());
                    pst.executeUpdate();
                    cn.close();

                    txt_confirmacion.setBackground(new Color(188, 245, 144));
                    txt_nuevaContraseña.setBackground(new Color(188, 245, 144));
                    JOptionPane.showMessageDialog(null, "Contraseña modificada correctamente.");
                    this.dispose();

                } catch (SQLException e) {
                    System.out.println("Error al modificar contraseña " + e);
                    JOptionPane.showMessageDialog(null, "Error en la conexión con la base de datos. Contacte con el Administrador del sistema.");
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(ModificarContraseña.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                txt_confirmacion.setBackground(Color.pink);
                JOptionPane.showMessageDialog(null, "Confirmación incorrecta. Inténtelo de nuevo.", "", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            txt_nuevaContraseña.setBackground(Color.pink);
            txt_confirmacion.setBackground(Color.pink);
            JOptionPane.showMessageDialog(null, "Ambos campos son obligatorios.", "", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_actualizarActionPerformed

