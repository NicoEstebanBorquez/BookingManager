    private void jButton_anadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_anadirActionPerformed

        Usuario usuario = new Usuario();
        usuario.setUsername(txt_username.getText().trim());
        usuario.setPassword(txt_password.getText().trim());
        usuario.setNombre(txt_nombre.getText().trim());
        usuario.setApellidos(txt_apellidos.getText().trim());
        usuario.setTelefono(txt_tlf.getText().trim());
        usuario.setEmail(txt_email.getText().trim());

        int puesto_int;
        puesto_int = combo_puesto.getSelectedIndex() + 1;

        int validacion = 0;

        //Comprobación de que todos los campos han sido rellenados
        if (usuario.getUsername().equals("")) {
            txt_username.setBackground(new Color(255, 82, 82));
            validacion++;
        }
        if (usuario.getPassword().equals("")) {
            txt_password.setBackground(new Color(255, 82, 82));
            validacion++;
        }
        if (usuario.getNombre().equals("")) {
            txt_nombre.setBackground(new Color(255, 82, 82));
            validacion++;
        }
        if (usuario.getApellidos().equals("")) {
            txt_apellidos.setBackground(new Color(255, 82, 82));
            validacion++;
        }
        if (usuario.getTelefono().equals("")) {
            txt_tlf.setBackground(new Color(255, 82, 82));
            validacion++;
        }
        if (usuario.getEmail().equals("")) {
            txt_email.setBackground(new Color(255, 82, 82));
            validacion++;
        }

        //Asignando a cada valor INT del combobox un valor String para meterlo en la BD
        if (puesto_int == 1) {
            usuario.setPuesto("Manager");
        } else if (puesto_int == 2) {
            usuario.setPuesto("Receptionist");
        } else if (puesto_int == 3) {
            usuario.setPuesto("Reservation sales");
        }

        //Consulta para comprobar que el username elegido está disponible
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/on_reservationssoftware", "root", "");
            PreparedStatement pst = cn.prepareStatement("select username from usuarios where username = '" + usuario.getUsername() + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txt_username.setBackground(new Color(255, 82, 82));
                JOptionPane.showMessageDialog(null, "Username not available.");
                cn.close();
            } else {
                cn.close();

                if (validacion == 0) {
                    Class.forName("com.mysql.jdbc.Driver");
                    Connection cn2 = DriverManager.getConnection("jdbc:mysql://localhost/on_reservationssoftware", "root", "");
                    PreparedStatement pst2 = cn2.prepareStatement("insert into usuarios values (?,?,?,?,?,?,?,?,?)");

                    pst2.setInt(1, 0);
                    pst2.setString(2, usuario.getUsername());
                    pst2.setString(3, usuario.getPassword());
                    pst2.setString(4, usuario.getNombre());
                    pst2.setString(5, usuario.getApellidos());
                    pst2.setString(6, usuario.getTelefono());
                    pst2.setString(7, usuario.getPuesto());
                    pst2.setString(8, usuario.getEmail());
                    pst2.setString(9, "Activo");

                    pst2.executeUpdate(); //Ejecutar sentencia
                    cn2.close();

                    Limpiar();
                    txt_username.setBackground(new Color(188, 245, 144));
                    txt_password.setBackground(new Color(188, 245, 144));
                    txt_nombre.setBackground(new Color(188, 245, 144));
                    txt_apellidos.setBackground(new Color(188, 245, 144));
                    txt_email.setBackground(new Color(188, 245, 144));
                    txt_tlf.setBackground(new Color(188, 245, 144));

                    JOptionPane.showMessageDialog(null, "New user successfully added.");
                } else {
                    JOptionPane.showMessageDialog(null, "Please complete all fields.");
                }
            }
        } catch (SQLException e) {
            System.err.println("Ha ocurrido un error al consultar si el Username existe en la BD " + e);
            JOptionPane.showMessageDialog(null, "Error en la conexión con la base de datos. Contacte con el Administrador del sistema.");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AnadirUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }

        GestionarUsuarios gu = new GestionarUsuarios();
        gu.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButton_anadirActionPerformed

